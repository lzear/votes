import { solve } from './index'

it('solves 2', () => {
  expect(
    solve([
      [0, 1],
      [-1, 0],
    ]),
  ).toEqual([1, 0])
})

it('solves 3', () => {
  expect(
    solve([
      [0, 1, -1],
      [-1, 0, 1],
      [1, -1, 0],
    ]),
  ).toEqual([0.3333333333333333, 0.3333333333333333, 0.3333333333333333])
})

it('solves 4', () => {
  expect(
    solve([
      [0, 1, -1, 1],
      [-1, 0, 1, 1],
      [1, -1, 0, 1],
      [-1, -1, -1, 0],
    ]),
  ).toEqual([0.3333333333333333, 0.3333333333333333, 0.3333333333333333, 0])
})
